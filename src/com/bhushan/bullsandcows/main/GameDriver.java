package com.bhushan.bullsAndCows.main;

import com.bhushan.bullsAndCows.generateNumber.PlayAsNumberGenerator;
import com.bhushan.bullsAndCows.utils.ScannerDependencyInjection;

public class GameDriver 
{
	public static void startNewGame()
	{
		System.out.println("\n\n**Starting new Game** \n"
				+ "Choose an option\n"
				+ "1. Guess a number generated by the Bulls and Cows Wizard! \n"
				+ "2. Test Wizard's smarts as he guesses your number!");
		int choice=ScannerDependencyInjection.getInstance().nextInt();
		while(choice!=1 && choice!=2)
		{
			System.out.println("Sorry the Wizard only accepts two options\n"
					+ "Choose an option\n"
					+ "1. Guess a number generated by the Bulls and Cows Wizard! \n"
					+ "2. Test Wizard's smarts as he guesses your number!");
			choice=ScannerDependencyInjection.getInstance().nextInt();
		}
		
		if(choice==1)
		{
			PlayAsNumberGenerator game =new PlayAsNumberGenerator();
			game.playGame();
		}
		else
		{
			//TODO
		}
		
		
	}
	public static void runGame()
	{
		boolean runGame=true;
		System.out.println("Hello!!\n"
				+ "Ready for your first game ?\n"
				+ "Ready or not Here it comes!!!");
		while(runGame)
		{
			GameDriver.startNewGame();
			System.out.println("\nGo for another round?\n"
					+ "1. Yes!!\n"
					+ "2. I am done playing.");
			int choice=ScannerDependencyInjection.getInstance().nextInt();
			while(choice!=1 && choice!=2)
			{
				System.out.println("Sorry the Wizard only accepts two options\n"
						+ "Choose an option\n"
						+ "Go for another round?\n"
						+"1. Yes!!\n"
						+ "2. I am done playing.");
				choice=ScannerDependencyInjection.getInstance().nextInt();
			}
			runGame=choice==1?true:false;
			
		}
		System.out.println("Thanks for Playing \n"
				+ "Hope you had fun!!");
	}
	
	public static void main(String[] args)
	{
		GameDriver.runGame();
	}
}
